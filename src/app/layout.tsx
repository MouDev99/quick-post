import type { Metadata } from "next";
import { Roboto_Mono } from "next/font/google";
import "./globals.css";
import SideNav from "./ui/sidenav";
import { auth } from "@/auth";
import { EdgeStoreProvider } from "./lib/edgestroe";
import SessionProvider from './session-provider';
import RightSideBar from "./ui/right-sidebar";
import ModalProvider from "./ui/modal";
import { fetchUsersToFollow } from "./lib/data";

const robot_mono = Roboto_Mono({
  subsets: ['latin'],
  variable: '--font-roboto-mono',
  display: 'swap',
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const session = await auth();
  const user = session?.user

  const usersToFollow = await fetchUsersToFollow(user?.id);

  const isLoggedIn = user ? true : false;

  return (
    <html lang="en">
      <body className={robot_mono.className}>
        <EdgeStoreProvider>
          <SessionProvider session={session}>
            <ModalProvider>
              <div className="flex justify-center px-1 sm:px-6 h-fit bg-white">
                {isLoggedIn && <SideNav />}
                <div className={isLoggedIn ? 'ml-1 border-x border-gray-200 max-w-[580px] w-full sm:w-4/5 md:w-3/3' : ''}>
                  {children}
                </div>
                {isLoggedIn &&
                 <RightSideBar
                   usersToFollow={usersToFollow}
                 />
                }
              </div>
            </ModalProvider>
          </SessionProvider>
        </EdgeStoreProvider>
      </body>
    </html>
  );
}
